function zcGetLang() {
  const saved = localStorage.getItem('zc_lang');
  if (saved === 'tr' || saved === 'en') return saved;
  return (navigator.language || 'tr').toLowerCase().startsWith('en') ? 'en' : 'tr';
}

function zcApplyLang(lang) {
  const dict = ZC_I18N[lang] || ZC_I18N.tr;
  document.documentElement.lang = lang;
  localStorage.setItem('zc_lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  const titleEl = document.querySelector('title[data-i18n-title]');
  if (titleEl) {
    const key = titleEl.getAttribute('data-i18n-title');
    if (dict[key] !== undefined) titleEl.textContent = dict[key];
  }
  const metaDesc = document.querySelector('meta[name="description"][data-i18n-content]');
  if (metaDesc) {
    const key = metaDesc.getAttribute('data-i18n-content');
    if (dict[key] !== undefined) metaDesc.setAttribute('content', dict[key]);
  }
  document.querySelectorAll('.lang-toggle button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Re-render any live feeds already on the page so dynamic content
  // (GitHub activity, Medium posts) switches language too.
  document.dispatchEvent(new CustomEvent('zc:langchange', { detail: { lang } }));
}

function zcInitLangToggle() {
  const lang = zcGetLang();
  zcApplyLang(lang);
  document.querySelectorAll('.lang-toggle button').forEach(btn => {
    btn.addEventListener('click', () => zcApplyLang(btn.dataset.lang));
  });
}

document.addEventListener('DOMContentLoaded', zcInitLangToggle);
